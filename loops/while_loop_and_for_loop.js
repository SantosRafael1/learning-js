let i = 0
//while the contidion is true, the code block will be executed
while(i < 3) {
    console.log(i);
    i++ //the value must be incremented
}

//decrementing a value
console.log("===========")
i = 3
while(i) {
    console.log(i)
    i--;
}

//do while
console.log("=========")
i = 0
do{
    console.log(i)
    i++
} while(i < 5)

//for loop
console.log("===for loop===")

for(let i = 0; i < 5; i++) {
    console.log(i)
}
